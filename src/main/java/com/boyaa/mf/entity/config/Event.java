public class Event{
	private int id;  //事件ID
	private int eventCateId;  //所属事件类型ID
	private String ename;  //事件英文名称
	private String name;  //事件名称
	private int plat;  //事件所属平台ID
	private int sid;  //事件所属站点
	private int status;  //是否还在启用，0为启用，1为停用

	public Event() {
		super();
	}

	public Event(int id, int eventCateId, String ename, String name,
			int plat, int sid, int status) {
		super();
		this.id = id;
		this.eventCateId = eventCateId;
		this.ename = ename;
		this.name = name;
		this.plat = plat;
		this.sid = sid;
		this.status = status;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getEventCateId() {
		return eventCateId;
	}

	public void setEventCateId(int eventCateId) {
		this.eventCateId = eventCateId;
	}

	public String getEname() {
		return ename;
	}

	public void setEname(String ename) {
		this.ename = ename;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPlat() {
		return plat;
	}

	public void setPlat(int plat) {
		this.plat = plat;
	}

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
}
