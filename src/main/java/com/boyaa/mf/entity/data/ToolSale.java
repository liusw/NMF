public class ToolSale {
	private int plat;
	private int tm;
	private int pcard;
	private int num;
	private int money;
	private long chips;
	public int getPlat() {
		return plat;
	}
	public void setPlat(int plat) {
		this.plat = plat;
	}
	public int getTm() {
		return tm;
	}
	public void setTm(int tm) {
		this.tm = tm;
	}
	public int getPcard() {
		return pcard;
	}
	public void setPcard(int pcard) {
		this.pcard = pcard;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public long getChips() {
		return chips;
	}
	public void setChips(long chips) {
		this.chips = chips;
	}
	
}
