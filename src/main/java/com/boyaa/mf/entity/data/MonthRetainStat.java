public class MonthRetainStat {
	private int tm;
	private int dayActive;//日活跃
	private int lastMonthRetain;//上月留存
	private int threeMonthRetain;//3月前那个月的留存
	private int sixMonthRetain;//6月前那个月的留存
	private int twelveMonthRetain;//12月前那个月的留存
	public int getTm() {
		return tm;
	}
	public void setTm(int tm) {
		this.tm = tm;
	}
	public int getDayActive() {
		return dayActive;
	}
	public void setDayActive(int dayActive) {
		this.dayActive = dayActive;
	}
	public int getLastMonthRetain() {
		return lastMonthRetain;
	}
	public void setLastMonthRetain(int lastMonthRetain) {
		this.lastMonthRetain = lastMonthRetain;
	}
	public int getThreeMonthRetain() {
		return threeMonthRetain;
	}
	public void setThreeMonthRetain(int threeMonthRetain) {
		this.threeMonthRetain = threeMonthRetain;
	}
	public int getSixMonthRetain() {
		return sixMonthRetain;
	}
	public void setSixMonthRetain(int sixMonthRetain) {
		this.sixMonthRetain = sixMonthRetain;
	}
	public int getTwelveMonthRetain() {
		return twelveMonthRetain;
	}
	public void setTwelveMonthRetain(int twelveMonthRetain) {
		this.twelveMonthRetain = twelveMonthRetain;
	}
	
}
